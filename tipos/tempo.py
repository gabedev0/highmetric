def segundos_para_minutos(segundos):
    return segundos / 60


def minutos_para_segundos(minutos):
    return minutos * 60


def minutos_para_horas(minutos):
    return minutos / 60


def horas_para_minutos(horas):
    return horas * 60


def horas_para_dias(horas):
    return horas / 24


def dias_para_horas(dias):
    return dias * 24
