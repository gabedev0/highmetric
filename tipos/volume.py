def litros_para_mililitros(litros):
    return litros * 1000

def mililitros_para_litros(mililitros):
    return mililitros / 1000

def litros_para_galoes(litros):
    return litros / 3.78541

def galoes_para_litros(galoes):
    return galoes * 3.78541

def xicaras_para_mililitros(xicaras):
    return xicaras * 240

def mililitros_para_xicaras(mililitros):
    return mililitros / 240

def oncas_liquidas_para_litros(oncas_liquidas):
    return oncas_liquidas / 33.814

def litros_para_oncas_liquidas(litros):
    return litros * 33.814