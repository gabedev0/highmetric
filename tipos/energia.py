def joules_para_quilocalorias(joules):
    return joules / 4184

def quilocalorias_para_joules(quilocalorias):
    return quilocalorias * 4184

def joules_para_quilowatt_horas(joules):
    return joules / 3600000

def quilowatt_horas_para_joules(quilowatt_horas):
    return quilowatt_horas * 3600000
