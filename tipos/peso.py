def quilogramas_para_gramas(quilogramas):
    return quilogramas * 1000

def gramas_para_quilogramas(gramas):
    return gramas / 1000

def quilogramas_para_libras(quilogramas):
    return quilogramas * 2.20462

def libras_para_quilogramas(libras):
    return libras / 2.20462

def toneladas_para_quilogramas(toneladas):
    return toneladas * 1000

def quilogramas_para_toneladas(quilogramas):
    return quilogramas / 1000

def oncas_para_gramas(oncas):
    return oncas * 28.3495

def gramas_para_oncas(gramas):
    return gramas / 28.3495