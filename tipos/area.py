def metros_quadrados_para_hectares(metros_quadrados):
    return metros_quadrados / 10000


def hectares_para_metros_quadrados(hectares):
    return hectares * 10000


def metros_quadrados_para_acres(metros_quadrados):
    return metros_quadrados / 4046.86


def acres_para_metros_quadrados(acres):
    return acres * 4046.86


def pes_quadrados_para_metros_quadrados(pes_quadrados):
    return pes_quadrados * 0.092903


def metros_quadrados_para_pes_quadrados(metros_quadrados):
    return metros_quadrados / 0.092903
